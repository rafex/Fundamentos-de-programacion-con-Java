package mx.rafex.cursos.fundamentos.ejercicios;

public class Calculadora {

    public Double suma(final Double a, final Double b) {
        return a + b;
    }

    public Double resta(final Double a, final Double b) {
        return a - b;
    }

    public Double division(final Double a, final Double b) {
        return a / b;
    }

    public Double multiplicacion(final Double a, final Double b) {
        return a * b;
    }

}
